namespace SunamoDevCode.Enums;

/// <summary>
/// Defines the parts of an XLF (XLIFF) translation file element.
/// </summary>
public enum XlfParts
{
    /// <summary>
    /// The identifier of the translation unit.
    /// </summary>
    Id,
    /// <summary>
    /// The target (translated) text of the translation unit.
    /// </summary>
    Target,
    /// <summary>
    /// The source (original) text of the translation unit.
    /// </summary>
    Source
}